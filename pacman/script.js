var alive = true;
var counter = 0;
var score = 0;
function update() {
	if (!dots.includes(1)) {
		alert("You Win!\nScore: " + score + "\nPlay Again?");
		resetGame();
	} else {
		if (alive) {
			counter++;
			updateCherry();
			movePlayer();
			eatDots();
			updatePoints();
			updateGhosts();
			render();
		} else {
			alert("You Lost\nScore: " + score + "\nClick OK to Restart");
			resetGame();
		}
	}
}

var cherryTime = 0;
function updateCherry() {
	cherryTime++;
	if (cherry) {
		if (playerX === 13 && playerY === 17) {
			cherry = false;
			score += 500;
			points.push({num: "+500", life: 0, x: scale*playerX+rand(-scale, scale), y: scale*playerY+rand(-scale, scale)});
			cherryTime = 0;
		}
		if (counter % rand(20, 50) === 0 && cherryTime > rand(50, 200)) {
			cherry = false;
			cherryTime = 0;
		}
	} else {
		if (counter % rand(20, 50) === 0 && cherryTime > rand(100, 200)) {
			cherry = true;
			cherryTime = 0;
		}
	}
}

function updateGhosts() {
	var options;
	for (var i = 0; i < ghosts.length; i++) {
		if ((ghosts[i].x === playerX) && (ghosts[i].y === playerY)) {
			alive = false;
		}
		//Can turn?
		options = [];
		for (var j = 0; j < 4; j++) {
			if (j !== ghosts[i].d && j !== (ghosts[i].d+2)%4) {
				newPX = ghosts[i].x - (j)%2 * (j-2);
				newPY = ghosts[i].y + (j+1)%2 * (j-1);
				if (map[newPY*mapWidth + newPX] === 0) {
					options.push(j);
				}
			}
		}
		if (options.length > 0) {
			ghosts[i].d = options[rand(0, options.length-1)];
		}
		
		//Can move forward?
		newPX = ghosts[i].x - (ghosts[i].d)%2 * (ghosts[i].d-2);
		newPY = ghosts[i].y + (ghosts[i].d+1)%2 * (ghosts[i].d-1);
		if (newPX < 0) {
			newPX = mapWidth-1;
		} else if (newPX >= mapWidth) {
			newPX = 0;
		}
		if (map[newPY*mapWidth + newPX] === 0) {
			ghosts[i].x = newPX;
			ghosts[i].y = newPY;
		}
		if ((ghosts[i].x === playerX) && (ghosts[i].y === playerY)) {
			alive = false;
		}
	}
}

var newPX;
var newPY;
function movePlayer() {
	//Can turn?
	newPX = playerX - (direction)%2 * (direction-2);
	newPY = playerY + (direction+1)%2 * (direction-1);
	if (map[newPY*mapWidth + newPX] === 0) {
		playerDirection = direction;
	}
	//Can move forward?
	newPX = playerX - (playerDirection)%2 * (playerDirection-2);
	newPY = playerY + (playerDirection+1)%2 * (playerDirection-1);
	if (newPX < 0) {
		newPX = mapWidth-1;
	} else if (newPX >= mapWidth) {
		newPX = 0;
	}
	if (map[newPY*mapWidth + newPX] === 0) {
		playerX = newPX;
		playerY = newPY;
	}
}

